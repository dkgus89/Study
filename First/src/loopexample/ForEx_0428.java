package loopexample;

public class ForEx_0428 {
	public static void main(String[] args) {
		int i;
		int sum;
		for(i=1, sum=0; i<=10; i++) {
			sum += i;
		}
		System.out.println("1부터 10까지의 합은 "+sum+"입니다.");

		System.out.println("...................복습");
		
		int num;
		for(num=1;num<=5;num++) {
			System.out.println(num);
		}
		
		System.out.println("...................복습");
		
		int s;
		for(s=1;s<=10;s++) {
			System.out.println("안녕하세요"+s);
		}
	}
}
